// Задаємо одновимірний масив дійсних чисел
let array = [3, -2, 0, 5, -1, 8, -4, 7, 2];

// Ініціалізуємо лічильник для додатних елементів
let positiveCount = 0;

// Перевіряємо кожен елемент масиву
for (let i = 0; i < array.length; i++) {
    if (array[i] > 0) {
        positiveCount++;
    }
}

console.log(`Кількість додатних елементів у масиві: ${positiveCount}`);
